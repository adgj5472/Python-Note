# Concatenation
list1 = [2, 4]
list2 = [6, 8, 10]
print("list1 = " + str(list1))
print("list2 = " + str(list2))
list3 = list1 + list2
print("list3=list1+list2 = " + str(list3))

# Repetition
list4 = list1 * 3
print("list4=list1*3 = " + str(list4))

# Member
print(8 in list2)       # True
print(2 not in list1)   # False
