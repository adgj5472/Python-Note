# list
list1 = [4, 2, 8, 9, 1, 8]
print("list1 = " + str(list1))
s = list1.count(8)
print("list1.count(8) = " + str(s))
s = list1.index(8)
print("list1.index(8) = " + str(s))
s = list1.index(1)
print("list1.index(1) = " + str(s))
list1.sort()
print("list1.sort() = " + str(list1))
list1.reverse()
print("list1.reverse() = " + str(list1))